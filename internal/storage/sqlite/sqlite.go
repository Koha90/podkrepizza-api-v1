package sqlite

import (
	"database/sql"
	"fmt"

	_ "github.com/mattn/go-sqlite3"
)

type Storage struct {
	db *sql.DB
}

func New(storagePath string) (*Storage, error) {
	const op = "storage.sqlite.NewStorage"

	db, err := sql.Open("sqlite3", storagePath)
	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	stmt, err := db.Prepare(`
    CREATE TABLE IF NOT EXISTS Pizzas (
      id INT PRIMARY KEY,
      title VARCHAR(50) UNIQUE,
      description TEXT,
      price DECIMAL(4, 2));
  `)

	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	_, err = stmt.Exec()
	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	stmt, err = db.Prepare(`
    CREATE TABLE IF NOT EXISTS Ingredients (
      id INT PRIMARY KEY,
      title VARCHAR(50) UNIQUE);
  `)

	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	_, err = stmt.Exec()
	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	stmt, err = db.Prepare(`
    CREATE TABLE IF NOT EXISTS PizzaIngredients (
      pizza_id INT,
      ingredient_id INT);
  `)

	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	_, err = stmt.Exec()
	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	stmt, err = db.Prepare(`
    CREATE TABLE IF NOT EXISTS Users (
      id INT PRIMARY KEY,
      name VARCHAR(50),
      email VARCHAR(50),
      password VARCHAR(255));
  `)

	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	_, err = stmt.Exec()
	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	stmt, err = db.Prepare(`
    CREATE TABLE IF NOT EXISTS Orders (
      id INT PRIMARY KEY,
      user_id INT,
      order_time DATETIME,
      paid BOOLEAN
    );
  `)

	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	_, err = stmt.Exec()
	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	stmt, err = db.Prepare(`
    CREATE TABLE IF NOT EXISTS Cart (
      id INT PRIMARY KEY,
      pizza_id INT,
      order_id INT,
      quantity INT
    );
  `)

	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	_, err = stmt.Exec()
	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	return &Storage{db: db}, nil
}
